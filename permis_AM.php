<p><del class="gras">MOB 60</del>, en partenariat avec <del class="gras">l'auto-école Drive-up</del>, <del class="gras">La Direction Départementale Territoriale de l'Oise</del> et l'<del class="gras">ANPAA</del> propose aux jeunes nés après décembre 1987, une formation à la conduite d'un deux-roues dans l'objectif d'obtenir le permis AM (BSR) mais aussi de sensibiliser le jeune à tout les dangers de la route et à l'importance de circuler en règle par rapport à la législation.</p>

<p>Sur la base d'une formation collective, plusieurs modules sont proposés :</p>

<ul>
<li>Prévention routière, code de la route, respect des règles de <br/><br/> sécurité. Utilisation d'un simulateur de conduite,</li>
<li>Prévention aux conduites à risques (alcool, cannabis...),</li>
<li>Mécanique de base (maintenance, apprendre à faire face à une <br/><br/> situation de panne), les équipements obligatoires du deux roues <br/><br/> et du conducteur</li>
<li>Formation pratique du permis AM (plateau et circulation)</li>
</ul>

<p class="gras">La formation pratique sera dispensée par un moniteur spécialisé deux roues de  l'auto-école de Voinsilieu de Beauvais.</p>


